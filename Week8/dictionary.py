# DICTIONARY a collection of key-value pairs that are unordered, mutable, and indexed
# Dictionaries are used to store data values in key:value pairs.
my_dict = {
    'banana' : 7,
    'apple' : 30,
    'grape' : 69
}
print(my_dict['grape'])

my_dict['apple'] = 21
print(my_dict)

my_dict['banana'] = 14
print(my_dict)
