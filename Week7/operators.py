# OPERATORS

# Addition (+)
# Subtraction (-)
# Multiplication (*)
# Division (/)
# Modulus (%)
# Exponentiation (**)

x = 10
y = 3

# Addition
print(x + y)

# Subtraction
print(x - y)

# Multiplication
print(x * y)

# Division
print(x / y)

# Modulus
print(x % y)

# Exponentiation
print(x ** y)
 

 
